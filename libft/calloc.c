#include <stdlib.h>
#include <stddef.h>

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	i;

	ptr = malloc(count * size);
	if (!ptr)
		return (0);
	i = -1;
	while (++i < count * size) 
		*((unsigned char *)ptr + i) = 0;
	return (ptr);
}	
